async def seed() -> None:
    await init_db()
    async with async_session() as session:
        disc_repo = DisciplineRepository(session)
        q_repo = QuestionRepository(session)
        for name, description, questions in SEED_DATA:
            existing = await disc_repo.get_by_name(name)
            if existing is not None:
                print(f"Пропускаю: дисципліна '{name}' уже існує.")
                continue
            discipline = await disc_repo.create(name, description)
            for q_text, answers in questions:
                await q_repo.create_with_answers(
                    discipline_id=discipline.id, text=q_text, answers=answers
                )
            print(f"Створено '{name}' з {len(questions)} питаннями.")


if __name__ == "__main__":
    asyncio.run(seed())
